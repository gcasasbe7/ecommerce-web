<script>
export default {
  data() {
    return {
      cart: {
        items: [],
      }
    }
  },
  beforeCreate() {
    this.$store.commit('initializeStore')
  },
  mounted() {
    this.cart = this.$store.state.cart
  },
  computed: {
    cartSize() {
        return this.cart.items.reduce((acc, val) => {
            return acc += val.quantity
        }, 0)
    }
